#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char			res;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n >= 10)
	{
		res = (n % 10) + '0';
		ft_putnbr_fd(n / 10, fd);
	}
	else
		res = (n % 10) + '0';
	ft_putchar_fd(res, fd);
}
