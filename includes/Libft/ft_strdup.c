#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char			*s2;
	unsigned int	i;
	unsigned int	size;

	i = 0;
	size = ft_strlen((char *)s1);
	s2 = (char *)malloc(size * sizeof(char) + 1);
	if (s2 == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
